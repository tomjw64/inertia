#[typeshare]
pub type ExpandedBitBoard = [bool; 256];

#[derive(Copy, Clone, PartialEq)]
pub struct BitBoard(pub(crate) U256);

impl fmt::Debug for BitBoard {
  fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
    let rows = (0..16).map(|row_index| {
      let low_byte = self.0.byte(row_index * 2) as u16;
      let high_byte = self.0.byte(row_index * 2 + 1) as u16;
      low_byte + (high_byte << 8)
    });
    f.write_str("\n")?;
    for row in rows {
      let mut squares = format!("{:016b}", row)
        .chars()
        .map(|c| c.to_digit(10).unwrap().to_string())
        .collect::<Vec<_>>();
      squares.reverse();
      f.write_str("[")?;
      f.write_str(&squares.join(", "))?;
      f.write_str("]\n")?;
    }
    Ok(())
  }
}

impl From<Square> for BitBoard {
  fn from(square: Square) -> Self {
    let relevant_u64_index = square.0 / 64;
    let relevant_bit_index = square.0 % 64;
    let relevant_u64 = 1 << relevant_bit_index;
    let mut data = [0, 0, 0, 0];
    data[relevant_u64_index as usize] = relevant_u64;
    BitBoard::new(data)
  }
}

impl BitBoard {
  pub const ZERO: Self = Self(U256([0, 0, 0, 0]));
  pub(crate) const ONE: Self = Self(U256([1, 0, 0, 0]));
  pub(crate) const MAX: Self = Self(U256::MAX);

  pub(crate) const fn new(data: [u64; 4]) -> Self {
    Self(U256(data))
  }

  pub fn is_zero(&self) -> bool {
    self.0.is_zero()
  }

  pub(crate) fn set_bit(&mut self, bit_index: usize) {
    self.0 |= Self::ONE.0 << bit_index;
  }

  pub(crate) fn bit(&self, bit_index: usize) -> bool {
    self.0.bit(bit_index)
  }

  pub(crate) fn trailing_zeros(&self) -> u32 {
    // Board is LE, but U256 is BE
    self.0.leading_zeros()
  }

  pub(crate) fn leading_zeros(&self) -> u32 {
    // Board is LE, but U256 is BE
    self.0.trailing_zeros()
  }

  pub(crate) fn last_one(&self) -> Option<u8> {
    match self.trailing_zeros() {
      256.. => None,
      index => Some(255 - index as u8),
    }
  }

  pub(crate) fn first_one(&self) -> Option<u8> {
    match self.leading_zeros() {
      256.. => None,
      index => Some(index as u8),
    }
  }

  pub(crate) fn set_bits(&self) -> impl Iterator<Item = usize> + '_ {
    (0..256).filter(|index| self.bit(*index))
  }

  pub fn to_expanded(&self) -> ExpandedBitBoard {
    let mut bools = [false; 256];
    for index in 0..256 {
      bools[index] = self.bit(index)
    }
    return bools;
  }
}

impl Not for BitBoard {
  type Output = BitBoard;

  fn not(self) -> Self::Output {
    BitBoard(self.0.not())
  }
}

impl BitOr<BitBoard> for BitBoard {
  type Output = BitBoard;

  fn bitor(self, rhs: BitBoard) -> Self::Output {
    BitBoard(self.0.bitor(rhs.0))
  }
}

impl BitOr<&BitBoard> for BitBoard {
  type Output = BitBoard;

  fn bitor(self, rhs: &BitBoard) -> Self::Output {
    BitBoard(self.0.bitor(rhs.0))
  }
}

impl BitAnd<BitBoard> for BitBoard {
  type Output = BitBoard;

  fn bitand(self, rhs: BitBoard) -> Self::Output {
    BitBoard(self.0.bitand(rhs.0))
  }
}

impl BitAnd<&BitBoard> for BitBoard {
  type Output = BitBoard;

  fn bitand(self, rhs: &BitBoard) -> Self::Output {
    BitBoard(self.0.bitand(rhs.0))
  }
}

